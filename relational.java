public class relational {
    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;

        if (num1 == num2) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }

        if (num1 <= num2) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }
    }
}
