public class Operator {
    public static void main(String[] args) {
        int num = 10;

        System.out.println("Number is : " + num);

        // increment ++
        // num++;
        System.out.println("Number after Incremental opetrator : " + num++);

        num--;
        System.out.println("Number after Decremental opetrator : " + num);

        num += 5;
        System.out.println("Number after adding 5 on Shorthand : " + num);

        num -= 2;
        System.out.println("Number after Substracting 2 on Shorthand : " + num);

        num /= 3;
        System.out.println("Number after Div 5 on Shorthand : " + num);

    }
}
